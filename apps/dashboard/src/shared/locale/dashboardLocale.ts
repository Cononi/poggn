export function resolveDashboardStageLabel(stage: string | null, dictionary: Record<string, string>): string {
  if (stage === "proposal") {
    return dictionary.filterProposal;
  }
  if (stage === "plan") {
    return dictionary.filterPlan;
  }
  if (stage === "task") {
    return dictionary.lanePlanTask;
  }
  if (stage === "implementation") {
    return dictionary.laneImplementation;
  }
  if (stage === "refactor") {
    return dictionary.laneRefactor;
  }
  if (stage === "qa") {
    return dictionary.filterQa;
  }

  return stage ?? dictionary.unknown;
}

export function resolveDashboardFlowStatusLabel(
  status: "done" | "current" | "revising" | "upcoming",
  dictionary: Record<string, string>
): string {
  if (status === "done") {
    return dictionary.flowDone;
  }
  if (status === "revising") {
    return dictionary.flowRevising;
  }
  if (status === "current") {
    return dictionary.flowCurrent;
  }
  return dictionary.flowUpcoming;
}

export function resolveDashboardTopicBucketLabel(
  bucket: "active" | "archive",
  dictionary: Record<string, string>
): string {
  return bucket === "active" ? dictionary.active : dictionary.archive;
}

export function resolveDashboardHealthLabel(value: string | null, dictionary: Record<string, string>): string {
  if (value === "ok") {
    return dictionary.ok;
  }
  if (value === "partial") {
    return dictionary.partial;
  }
  if (value === "complete") {
    return dictionary.complete;
  }

  return value ?? dictionary.unknown;
}

