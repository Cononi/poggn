    "    CLEANUP_STATUS=\"completed\"",
    "    CLEANUP_REASON=\"Removed the ai branch after promoting '$RELEASE_BRANCH'. $REMOTE_CLEANUP_REASON\"",
    "  elif [[ \"$LOCAL_CLEANUP_STATE\" == \"failed\" && \"$REMOTE_CLEANUP_STATE\" == \"failed\" ]]; then",
