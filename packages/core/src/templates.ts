function governedBranchRecoveryShellFunction(options: {
  helperLabel: "stage helper" | "publish helper";
  deferredMessage: string;
}): string[] {
  return [
    "attempt_working_branch_recovery() {",
    '  if [[ -z "$WORKING_BRANCH" || "$WORKING_BRANCH" == "pending" || "$BRANCH" == "$WORKING_BRANCH" ]]; then',
    "    return 0",
    "  fi",
    "",
    '  BRANCH_RECOVERY_STATUS="attempted"',
    '  BRANCH_RECOVERY_FROM="$BRANCH"',
    '  if git -C "$ROOT_DIR" checkout "$WORKING_BRANCH" >/dev/null 2>&1 || git -C "$ROOT_DIR" checkout -B "$WORKING_BRANCH" >/dev/null 2>&1; then',
    '    BRANCH="$(read_current_branch)"',
    '    BRANCH_RECOVERY_STATUS="recovered"',
    `    BRANCH_RECOVERY_REASON="Governed checkout recovered the ${options.helperLabel} onto '$WORKING_BRANCH' from '$BRANCH_RECOVERY_FROM'."`,
    "    return 0",
    "  fi",
    "",
    '  BRANCH_RECOVERY_STATUS="failed"',
    `  BRANCH_RECOVERY_REASON="Governed checkout to '$WORKING_BRANCH' failed from '$BRANCH_RECOVERY_FROM', so ${options.deferredMessage}."`,
    "  return 1",
    "}",
    ""
  ];
}

