export type TemplateLanguage = "ko" | "en";
export type TemplateAutoMode = "on" | "off";
export type TemplateTeamsMode = "on" | "off";
export type TemplateProvider = "codex";
import {
  CORE_WORKFLOW_SKILLS,
  MANDATORY_IMPLEMENTATION_CRITERIA_EN,
  MANDATORY_IMPLEMENTATION_CRITERIA_KO,
  OPTIONAL_AUDIT_SKILLS,
  WORKFLOW_FRONTMATTER_SKILLS,
  WORKFLOW_FRONTMATTER_STAGES,
  type WorkflowSkillName
} from "./workflow-contract.js";

export interface TemplateInput {
  language: TemplateLanguage;
  autoMode: TemplateAutoMode;
  teamsMode: TemplateTeamsMode;
  provider: TemplateProvider;
  version: string;
}

export interface GeneratedFileTemplate {
  path: string;
  content: string;
  executable?: boolean;
  preserveExistingContent?: boolean;
}

function lines(parts: string[]): string {
  return `${parts.join("\n")}\n`;
}

function numberedCoreWorkflowSkillLines(): string[] {
  return CORE_WORKFLOW_SKILLS.map((skill, index) => `${index + 1}. \`${skill}\``);
}

function optionalAuditSkillLines(): string[] {
  return OPTIONAL_AUDIT_SKILLS.map((skill) => `- \`${skill}\``);
}

function mandatoryCriteriaSection(language: TemplateLanguage): string[] {
  const title =
    language === "ko" ? "## Mandatory Implementation Criteria" : "## Mandatory Implementation Criteria";
  const criteria =
    language === "ko" ? MANDATORY_IMPLEMENTATION_CRITERIA_KO : MANDATORY_IMPLEMENTATION_CRITERIA_EN;

  return ["", title, "", ...criteria.map((criterion) => `- ${criterion}`)];
}

function verificationContractRule(language: TemplateLanguage): string {
  return language === "ko"
    ? "- 대상 프로젝트 검증 명령은 선언된 current-project verification contract가 있을 때만 자동 실행 후보가 되며, 없으면 `manual verification required`로 남긴다."
    : "- Treat project verification commands as auto-runnable only when a declared current-project verification contract exists; otherwise keep `manual verification required`.";
}

function auditApplicabilityExamples(language: TemplateLanguage): string[] {
  return language === "ko"
    ? [
        "- 형식 예시: `- [pgg-token]: [required|not_required] | <짧은 근거>`",
        "- 형식 예시: `- [pgg-performance]: [required|not_required] | <짧은 근거>`"
      ]
    : [
        "- Example format: `- [pgg-token]: [required|not_required] | <short rationale>`",
        "- Example format: `- [pgg-performance]: [required|not_required] | <short rationale>`"
      ];
}

function auditApplicabilityContractSection(language: TemplateLanguage): string[] {
  if (language === "ko") {
    return [
      "### Audit Applicability Contract",
      "",
      "- `plan.md`, `task.md`, `state/current.md`, `qa/report.md`에는 `Audit Applicability` 섹션을 두고 아래 형식을 맞춘다.",
      ...auditApplicabilityExamples(language)
    ];
  }

  return [
    "### Audit Applicability Contract",
    "",
    "- `plan.md`, `task.md`, `state/current.md`, and `qa/report.md` should share an `Audit Applicability` section.",
    ...auditApplicabilityExamples(language)
  ];
}

function auditApplicabilityStateRule(language: TemplateLanguage): string {
  return language === "ko"
    ? "- `Audit Applicability` 섹션의 상태와 짧은 근거를 최소 컨텍스트에 유지한다."
    : "- Preserve the `Audit Applicability` section with status and rationale in the minimum handoff.";
}

