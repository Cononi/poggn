    "NEXT_VERSION=\"$(node -e \"const current=(process.argv[1]||'0.0.0').split('.').map(Number); const type=process.argv[2]; let [major,minor,patch]=[current[0]||0,current[1]||0,current[2]||0]; if (type==='remove') { major += 1; minor = 0; patch = 0; } else if (type==='feat') { minor += 1; patch = 0; } else { patch += 1; } process.stdout.write([major,minor,patch].join('.'));\" \"$PREVIOUS_VERSION\" \"$ARCHIVE_TYPE\")\"",
    "TIMESTAMP=\"$(date -u +\"%Y-%m-%dT%H:%M:%SZ\")\"",
    "",
    "cat > \"$VERSION_FILE\" <<EOF",
    "{",
    "  \"topic\": \"$TOPIC\",",
    "  \"changeType\": \"$ARCHIVE_TYPE\",",
    "  \"version\": \"$NEXT_VERSION\",",
    "  \"previousVersion\": \"$PREVIOUS_VERSION\",",
